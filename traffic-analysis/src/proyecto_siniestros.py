# Realizar una unión espacial entre los datos de accidentes y las zonas geográficas
accidentes_por_zona_gdf = gpd.sjoin(gdf, zonas_gdf, how="left", predicate="within")

# Contar el número de accidentes por zona
accidentes_por_zona = accidentes_por_zona_gdf.groupby('DPA_ZONA').size().reset_index(name='NUM_ACCIDENTES')

# Unir los resultados con el GeoDataFrame de zonas
zonas_gdf = zonas_gdf.merge(accidentes_por_zona, how='left', on='DPA_ZONA', suffixes=('', '_y'))

# Eliminar la columna duplicada si existe
