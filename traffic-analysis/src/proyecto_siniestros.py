"""##Geodata frame"""

# Cargar los datos geográficos de las zonas
zonas_gdf = gpd.read_file("zonas_inec.geojson")

# Asegurarse de que ambos conjuntos de datos tengan el mismo CRS
gdf = gpd.GeoDataFrame(
    df, geometry=gpd.points_from_xy(df.LONGITUD_X, df.LATITUD_Y)).set_crs(epsg=4326)
