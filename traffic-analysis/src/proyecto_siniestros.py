"""En base a edad se creará una categoria donde se clasifique el grupo de edad de los siniestros. Se tomara las siguientes categorias como referencia https://economipedia.com/definiciones/segmentacion-por-edad.html

"""

# Define a function to categorize ages
def categorize_age(age):
    if age == "Edad Desconocida":
        return "Edad no registrada"
    elif age < 18:
        return "Menor de edad"
    elif 18 <= age <= 24:
        return "Joven"
    elif 25 <= age <= 39:
        return "Adulto joven"
    elif 40 <= age <= 54:
        return "Adulto"
    elif 55 <= age <= 64:
        return "Adulto mayor"
    elif age >= 65:
        return "Tercera edad"

# Apply the function to the EDAD column to create a new column CATEGORIA_EDAD
df['CATEGORIA_EDAD'] = df['EDAD'].apply(categorize_age)
